/**
 * Claude Code driver.
 *
 * Install path: COPY the worktree's claude-code plugin into the tmp
 * HOME's marketplace directory at `<tmpHome>/.claude/plugins/hivemind/`,
 * then let claude discover it via its normal plugin loader. We do NOT
 * use `--plugin-dir`. Earlier (commit history) we tried `--plugin-dir`,
 * but on a dev box that already had hivemind installed globally,
 * claude continued to load the GLOBAL bundle even with --plugin-dir
 * pointing at the worktree — possibly because settings.json hook
 * registrations are absolute-path and override session-only plugins.
 * Symptom: harness "passes" tests against the wrong (stale) bundle.
 *
 * By staging the worktree bundle inside the tmp HOME (which is the
 * only plugin location claude can see once HOME is overridden), the
 * harness guarantees the WORKTREE's code is what gets exercised.
 *
 * Session_id is generated by Claude Code at session start and isn't
 * exposed via stdout in a stable format. We read it out of the
 * hook-debug.log after the run; HIVEMIND_DEBUG=1 is set unconditionally
 * by the harness so this is guaranteed to exist.
 */

import { spawn } from "node:child_process";
import { readFileSync, existsSync, mkdirSync, cpSync, writeFileSync } from "node:fs";
import { join } from "node:path";
import type { AgentDriver, RunOpts, RunResult } from "../types.js";
import { parseCostCents } from "../cost.js";

const SESSION_LINE = /session=([0-9a-f-]{8,})/i;

export const claudeCodeDriver: AgentDriver = {
  id: "claude-code",
  providerKey: "ANTHROPIC_API_KEY",
  async install(home, repoRoot) {
    // Wire hivemind into the tmp HOME via settings.json hooks pointing
    // at the worktree's bundle paths (absolute). Replicating the full
    // claude plugin cache + marketplace + installed_plugins.json
    // registry would be brittle; settings.json hooks are the documented
    // mechanism and bypass plugin discovery entirely.
    //
    // Why not --plugin-dir: empirically on a dev box that ALSO has
    // hivemind globally installed at ~/.claude/plugins/, claude keeps
    // loading the global bundle's hooks (registered via the user's
    // real settings.json with absolute paths). With HOME overridden,
    // the global settings.json is bypassed, but `--plugin-dir` doesn't
    // wire SessionStart / PreToolUse / capture etc. — those need an
    // explicit hooks block in settings.json.
    //
    // We READ the canonical hooks.json from the plugin source and
    // rewrite ${CLAUDE_PLUGIN_ROOT} -> the absolute worktree path. Doing
    // so means the settings.json we write always matches what the plugin
    // ships, regardless of which entrypoints are added or removed.
    // Hard-coding the hook list here previously masked a bug where the
    // driver pointed Stop at a stop.js that doesn't exist in current
    // bundles — case 09 caught it; this rewrite makes the class of bug
    // impossible going forward.
    const pluginRoot = join(repoRoot, "claude-code");
    const hooksJsonPath = join(pluginRoot, "hooks", "hooks.json");
    const hooksJsonRaw = readFileSync(hooksJsonPath, "utf-8");
    const hooksJson = JSON.parse(
      hooksJsonRaw.replace(/\$\{CLAUDE_PLUGIN_ROOT\}/g, pluginRoot),
    );
    const settingsPath = join(home, ".claude", "settings.json");
    mkdirSync(join(home, ".claude"), { recursive: true });
    writeFileSync(settingsPath, JSON.stringify(hooksJson, null, 2));
    // Also stage the plugin tree so commands/skills are discoverable
    // when claude scans tmp HOME's plugin dir. Not strictly required
    // for hook firing (settings.json above handles that) but keeps
    // skillify auto-pull's pulled.json layout consistent with what
    // a real install would produce.
    const src = join(repoRoot, "claude-code");
    const dst = join(home, ".claude", "plugins", "hivemind");
    mkdirSync(dst, { recursive: true });
    for (const sub of [".claude-plugin", "bundle", "hooks", "skills", "commands"]) {
      const srcSub = join(src, sub);
      if (!existsSync(srcSub)) continue;
      cpSync(srcSub, join(dst, sub), { recursive: true });
    }
  },
  async run(prompt, opts): Promise<RunResult> {
    const args = [
      "-p",
      // Read-only run: don't allow Edit/Write — keeps a flaky model from
      // touching the sandbox in unexpected ways. The hivemind capture and
      // pre-tool-use hooks fire regardless of whether the model uses tools.
      "--allowedTools",
      "Bash,Read",
      "--model",
      "claude-haiku-4-5",
      prompt,
    ];
    const env: NodeJS.ProcessEnv = {
      ...process.env,
      HOME: opts.home,
      // Hivemind reads this in every hook to write hook-debug.log; the
      // session-id extraction in this driver depends on it being on.
      HIVEMIND_DEBUG: "1",
    };
    if (opts.providerEnv.ANTHROPIC_API_KEY) {
      env.ANTHROPIC_API_KEY = opts.providerEnv.ANTHROPIC_API_KEY;
    }
    return runProcess("claude", args, env, opts.timeoutMs ?? 90_000, opts.sessionId);
  },
};

/**
 * Spawn a CLI, capture stdout/stderr, and return a RunResult. Pulled out
 * of the driver so other drivers can share the spawn shape — only the
 * argv / env composition differs per agent.
 *
 * Falls back to `seedSessionId` if no `session=<sid>` line appears in
 * either stdout or the hook-debug.log. That fallback shouldn't normally
 * trigger; when it does, downstream SQL assertions will SELECT against
 * a session that doesn't exist and produce a clear "0 rows" failure
 * rather than a mysterious silent pass.
 */
export function runProcess(
  bin: string,
  args: string[],
  env: NodeJS.ProcessEnv,
  timeoutMs: number,
  seedSessionId: string,
): Promise<RunResult> {
  return new Promise((resolve) => {
    const startedAt = Date.now();
    const child = spawn(bin, args, { env, stdio: ["ignore", "pipe", "pipe"] });
    let stdout = "";
    let stderr = "";
    child.stdout.on("data", (chunk: Buffer) => { stdout += chunk.toString("utf-8"); });
    child.stderr.on("data", (chunk: Buffer) => { stderr += chunk.toString("utf-8"); });
    const killTimer = setTimeout(() => {
      child.kill("SIGKILL");
      stderr += `\n[harness] killed after ${timeoutMs}ms wall clock`;
    }, timeoutMs);
    child.on("exit", (code) => {
      clearTimeout(killTimer);
      const durationMs = Date.now() - startedAt;
      const home = env.HOME ?? "";
      const sessionId = extractSessionId(stdout, stderr, home) ?? seedSessionId;
      const agent = inferAgentFromBin(bin);
      const costCents = parseCostCents(agent, stdout);
      resolve({
        stdout,
        stderr,
        exitCode: code ?? -1,
        sessionId,
        costCents,
        durationMs,
      });
    });
    child.on("error", (err) => {
      clearTimeout(killTimer);
      const durationMs = Date.now() - startedAt;
      stderr += `\n[harness] spawn error: ${err.message}`;
      resolve({
        stdout,
        stderr,
        exitCode: -1,
        sessionId: seedSessionId,
        costCents: null,
        durationMs,
      });
    });
  });
}

function extractSessionId(stdout: string, stderr: string, home: string): string | null {
  // Try stdout first (some agents print it), then stderr, then the
  // hook-debug log where every hivemind hook writes one line per call.
  for (const haystack of [stdout, stderr]) {
    const m = haystack.match(SESSION_LINE);
    if (m) return m[1];
  }
  if (home) {
    const logPath = join(home, ".deeplake", "hook-debug.log");
    if (existsSync(logPath)) {
      const log = readFileSync(logPath, "utf-8");
      const m = log.match(SESSION_LINE);
      if (m) return m[1];
    }
  }
  return null;
}

function inferAgentFromBin(bin: string): import("../types.js").AgentId {
  // Used only by parseCostCents to pick the right regex. The driver
  // already knows its own id, but runProcess is exported for reuse so
  // we infer instead of threading the id through every callsite.
  if (bin === "claude") return "claude-code";
  if (bin === "codex") return "codex";
  if (bin === "cursor-agent") return "cursor-agent";
  if (bin === "hermes") return "hermes";
  return "pi";
}
